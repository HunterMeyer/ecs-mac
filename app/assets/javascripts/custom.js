$(document).on('page:load', function() {
  $('.button-link').hover(function() {
    $(this).children().removeClass('icon-white');
  }, function() {
    $(this).children().addClass('icon-white');
  });

  $('.btn-primary').hover(function() {
    $(this).children().removeClass('icon-white');
  }, function() {
    $(this).children().addClass('icon-white');
  });

});

$(document).ready(function() {
  $('.button-link').hover(function() {
    $(this).children().removeClass('icon-white');
  }, function() {
    $(this).children().addClass('icon-white');
  });

  $('.btn-primary').hover(function() {
    $(this).children().removeClass('icon-white');
  }, function() {
    $(this).children().addClass('icon-white');
  });
  
});